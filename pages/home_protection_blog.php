        <h2 class="title-II">What is Home Insurance?</h2>

        <p>
            Home insurance is a type of insurance that protects your home and the valuable items inside from various risks. It protects you from financial losses and helps you get back to normal life after any unfortunate event.
        </p>

        <p class="bold">Typically, home insurance covers the following:</p>

        <ul>
            <li>Damage from fire, explosion, short-circuit</li>
            <li>Natural disasters (earthquakes, floods, storms, etc.)</li>
            <li>Theft or burglary</li>
            <li>Any external accident such as a vehicle colliding with the house</li>
            <li>Damage to the structure of the house</li>
            <li>Protection of valuable belongings</li>
        </ul>

        <p class="bold">Some plans also offer additional coverage, such as:</p>

        <ul>
            <li>Damage to electronic devices</li>
            <li>Jewelry cover</li>
            <li>Temporary accommodation (if the house becomes uninhabitable)</li>
        </ul>

